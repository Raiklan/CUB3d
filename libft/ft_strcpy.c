#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	size_t	cur;

	cur = 0;
	if (s2 == NULL)
		return (s1);
	while (s2[cur] != '\0')
	{
		s1[cur] = s2[cur];
		cur++;
	}
	s1[cur] = '\0';
	return (s1);
}
